export const PLACEHOLDER_FOOD_IMAGE = 'https://images.unsplash.com/photo-1546069901-ba9599a7e63c?q=80&w=500&auto=format&fit=crop';

/**
 * Converte links de visualização do Google Drive em links diretos para imagem.
 * Ex: drive.google.com/file/d/ID/view -> drive.google.com/uc?export=view&id=ID
 */
export const formatImageUrl = (url: string | undefined): string => {
  if (!url) return PLACEHOLDER_FOOD_IMAGE;

  try {
    // Verifica se é um link do Google Drive
    if (url.includes('drive.google.com')) {
      // Extrai o ID do arquivo usando Regex
      const idMatch = url.match(/\/file\/d\/([^\/]+)/) || url.match(/id=([^\&]+)/);
      if (idMatch && idMatch[1]) {
        return `https://drive.google.com/uc?export=view&id=${idMatch[1]}`;
      }
    }
    return url;
  } catch (e) {
    return PLACEHOLDER_FOOD_IMAGE;
  }
};

